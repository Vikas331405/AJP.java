package AJP.java.com;

public class Datatypes {
	public static void main(String[]args) {
		int a=10;
		float b=1.6795f;
		byte c=2;
		long d=100000000l;

		System.out.println("a " +a);
		System.out.println("b " +b);
		System.out.println("c " +c);
		System.out.println("d " +d);
	
		
	}

}
